def fix_actors():
    """Исправление актёров в базе данных"""
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()
    
    # Очищаем старые связи
    cursor.execute("DELETE FROM movie_actors")
    
    updated = 0
    
    for code, actors in CORRECT_ACTORS.items():
        # Находим фильм по коду
        cursor.execute("SELECT id FROM movies WHERE code = ?", (code,))
        row = cursor.fetchone()
        
        if not row:
            print(f"❌ Фильм с кодом {code} не найден")
            continue
        
        movie_id = row[0]
        
        # Добавляем актёров
        for actor_name in actors:
            # Ищем или создаём актёра
            cursor.execute("SELECT id FROM actors WHERE name = ?", (actor_name,))
            actor_row = cursor.fetchone()
            
            if actor_row:
                actor_id = actor_row[0]
            else:
                # Создаём нового актёра
                cursor.execute("INSERT INTO actors (name) VALUES (?)", (actor_name,))
                actor_id = cursor.lastrowid
            
            # Создаём связь
            cursor.execute('''
                INSERT OR IGNORE INTO movie_actors (movie_id, actor_id) VALUES (?, ?)
            ''', (movie_id, actor_id))
        
        updated += 1
        print(f"✅ {code}. Актёры обновлены")
    
    conn.commit()
    conn.close()
    
    print(f"\n✅ Готово!")
    print(f"Обновлено фильмов: {updated}")


if __name__ == "__main__":
    print("🎭 Исправление актёров в базе данных...\n")
    fix_actors()
