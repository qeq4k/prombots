def get_predefined_rating(title: str) -> float:
    """
    Получить рейтинг из предустановленного списка.
    
    Args:
        title: Название фильма
    
    Returns:
        Рейтинг от 0.0 до 10.0 или 0.0 если не найдено
    """
    # Очищаем название для поиска
    clean_title = title.strip().lower()
    
    # Ищем точное совпадение
    if title in PREDEFINED_RATINGS:
        return PREDEFINED_RATINGS[title]
    
    # Ищем частичное совпадение
    for predefined_title, rating in PREDEFINED_RATINGS.items():
        if clean_title in predefined_title.lower() or predefined_title.lower() in clean_title:
            return rating
    
    # Рейтинг по умолчанию
    return 0.0


def get_rating_stars(rating: float) -> str:
    """Конвертирует рейтинг в звёзды"""
    if rating <= 0:
        return ""
    full_stars = int(rating / 2)  # 10 баллов = 5 звёзд
    return "⭐" * full_stars


